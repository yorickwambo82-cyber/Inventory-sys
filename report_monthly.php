<?php
session_start();
// Allow both admin and employee
if (!isset($_SESSION['logged_in']) || ($_SESSION['role'] !== 'employee' && $_SESSION['role'] !== 'admin')) {
    header("Location: login.php");
    exit();
}

require_once 'config/database.php';
$user_id = $_SESSION['user_id'];
$role = $_SESSION['role'];

$database = new Database();
$db = $database->getConnection();

// Get current month sales
if ($role === 'admin') {
    // Admin sees all sales for the month
    $query = "SELECT s.*, u.full_name as seller_name 
              FROM sales s 
              LEFT JOIN users u ON s.sold_by = u.user_id
              WHERE MONTH(s.sale_date) = MONTH(CURDATE()) AND YEAR(s.sale_date) = YEAR(CURDATE())
              ORDER BY s.sale_date DESC";
    $stmt = $db->prepare($query);
    $stmt->execute();
} else {
    // Employee sees only their sales
    $query = "SELECT s.*, :full_name as seller_name 
              FROM sales s 
              WHERE MONTH(s.sale_date) = MONTH(CURDATE()) AND YEAR(s.sale_date) = YEAR(CURDATE()) 
              AND s.sold_by = :user_id
              ORDER BY s.sale_date DESC";
    $stmt = $db->prepare($query);
    $stmt->bindParam(':user_id', $user_id);
    $stmt->bindValue(':full_name', $_SESSION['full_name']);
    $stmt->execute();
}

$month_sales = $stmt->fetchAll();

// Calculate Summaries
$payment_summary = [];
$weekly_summary = [];
$total_revenue = 0;

foreach ($month_sales as $sale) {
    // Payment Summary
    $method = ucfirst($sale['payment_method']);
    if (!isset($payment_summary[$method])) {
        $payment_summary[$method] = 0;
    }
    $payment_summary[$method] += $sale['sale_price'];
    
    // Weekly Summary (Group by Week Number)
    $week_num = date('W', strtotime($sale['sale_date']));
    if (!isset($weekly_summary[$week_num])) {
        $weekly_summary[$week_num] = ['count' => 0, 'total' => 0];
    }
    $weekly_summary[$week_num]['count']++;
    $weekly_summary[$week_num]['total'] += $sale['sale_price'];
    
    $total_revenue += $sale['sale_price'];
}

// Sort weekly summary by week number
ksort($weekly_summary);
